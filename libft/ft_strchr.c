#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	while (*s)
	{
		if (*s == (unsigned char)c)
			return ((char*)s);
		(char*)s++;
	}
	if (c == '\0')
		return ((char *)s);
	return (NULL);
}
